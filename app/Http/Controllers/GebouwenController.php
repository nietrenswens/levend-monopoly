    {
        $gebouw = new Gebouw;
        $gebouw->naam = $request->gebouw_naam;
        $gebouw->prijs = $request->gebouw_prijs;
        $gebouw->uuid = Str::random(40);
        $gebouw->save();
        return redirect(route('management'));
    }

